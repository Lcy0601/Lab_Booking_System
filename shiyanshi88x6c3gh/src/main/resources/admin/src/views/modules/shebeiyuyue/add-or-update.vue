					],
					shuliang: [
						{ validator: validateIntNumber, trigger: 'blur' },
					],
					fengmian: [
					],
					xinghao: [
					],
					jieyongshijian: [
					],
					xueshengxuehao: [
					],
					xueshengxingming: [
					],
					shoujihao: [
					],
					guihaizhuangtai: [
					],
					yongtu: [
					],
					sfsh: [
					],
					shhf: [
					],
				},
			};
		},
		props: ["parent"],
		computed: {
			sessionForm() {
				return JSON.parse(this.$storage.getObj('userForm'))
			},
			sessionTable() {
				return this.$storage.get('sessionTable')
			},



		},
		components: {
		},
		created() {
			this.ruleForm.jieyongshijian = this.getCurDateTime()
