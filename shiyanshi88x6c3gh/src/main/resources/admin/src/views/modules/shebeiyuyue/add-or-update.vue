		data() {
			var validateIntNumber = (rule, value, callback) => {
				if(!value){
					callback();
				} else if (!isIntNumer(value)) {
					callback(new Error("请输入整数"));
				} else {
					callback();
				}
			};
			return {
				id: '',
				type: '',
			
			
				ro:{
					yuyuebianhao : false,
					shebeibianhao : false,
					shebeimingcheng : false,
					shebeifenlei : false,
					yuanxi : false,
					shuliang : false,
