			download(file){
				window.open(`${file}`)
			},
			// 初始化
			init(id,type ) {
				if (id) {
					this.id = id;
					this.type = type;
				}
				if(this.type=='info'||this.type=='else'||this.type=='msg'){
					this.info(id);
				}else if(this.type=='logistics'){
					for(let x in this.ro) {
						this.ro[x] = true
					}
					this.logistics=false;
					this.info(id);
				}else if(this.type=='cross'){
					var obj = this.$storage.getObj('crossObj');
					for (var o in obj){
						if(o=='yuyuebianhao'){
							this.ruleForm.yuyuebianhao = obj[o];
							this.ro.yuyuebianhao = true;
							continue;
						}
						if(o=='shebeibianhao'){
							this.ruleForm.shebeibianhao = obj[o];
							this.ro.shebeibianhao = true;
							continue;
						}
						if(o=='shebeimingcheng'){
							this.ruleForm.shebeimingcheng = obj[o];
							this.ro.shebeimingcheng = true;
							continue;
						}
						if(o=='shebeifenlei'){
							this.ruleForm.shebeifenlei = obj[o];
							this.ro.shebeifenlei = true;
							continue;
						}
						if(o=='yuanxi'){
							this.ruleForm.yuanxi = obj[o];
							this.ro.yuanxi = true;
							continue;
						}
						if(o=='shuliang'){
							this.ruleForm.shuliang = obj[o];
							this.ro.shuliang = true;
							continue;
						}
						if(o=='fengmian'){
							this.ruleForm.fengmian = obj[o];
							this.ro.fengmian = true;
							continue;
						}
						if(o=='xinghao'){
							this.ruleForm.xinghao = obj[o];
							this.ro.xinghao = true;
							continue;
						}
						if(o=='jieyongshijian'){
							this.ruleForm.jieyongshijian = obj[o];
							this.ro.jieyongshijian = true;
							continue;
						}
						if(o=='xueshengxuehao'){
							this.ruleForm.xueshengxuehao = obj[o];
							this.ro.xueshengxuehao = true;
							continue;
						}
						if(o=='xueshengxingming'){
							this.ruleForm.xueshengxingming = obj[o];
							this.ro.xueshengxingming = true;
							continue;
						}
						if(o=='shoujihao'){
							this.ruleForm.shoujihao = obj[o];
							this.ro.shoujihao = true;
							continue;
						}
						if(o=='guihaizhuangtai'){
							this.ruleForm.guihaizhuangtai = obj[o];
							this.ro.guihaizhuangtai = true;
							continue;
						}
						if(o=='yongtu'){
							this.ruleForm.yongtu = obj[o];
							this.ro.yongtu = true;
							continue;
						}
					}
					this.ruleForm.shuliang = 0
					this.ro.shuliang = false
					this.ruleForm.guihaizhuangtai = '待归还'; 				}

				// 获取用户信息
				this.$http({
					url: `${this.sessionTable}/session`,
					method: "get"
				}).then(({ data }) => {
					if (data && data.code === 0) {
						var json = data.data;
						if(((json.xueshengxuehao!=''&&json.xueshengxuehao) || json.xueshengxuehao==0) && this.sessionTable!="users"){
							this.ruleForm.xueshengxuehao = json.xueshengxuehao
							this.ro.xueshengxuehao = true;
						}
						if(((json.xueshengxingming!=''&&json.xueshengxingming) || json.xueshengxingming==0) && this.sessionTable!="users"){
							this.ruleForm.xueshengxingming = json.xueshengxingming
							this.ro.xueshengxingming = true;
						}
						if(((json.shoujihao!=''&&json.shoujihao) || json.shoujihao==0) && this.sessionTable!="users"){
							this.ruleForm.shoujihao = json.shoujihao
							this.ro.shoujihao = true;
						}
						if(this.sessionTable!="users") {
							this.ro.guihaizhuangtai = true;
						}
					} else {
						this.$message.error(data.msg);
					}
				});
				this.guihaizhuangtaiOptions = "已归还,待归还".split(',')
			
			},
			// 多级联动参数

			async info(id) {
				await this.$http({
					url: `shebeiyuyue/info/${id}`,
					method: "get"
				}).then(({ data }) => {
					if (data && data.code === 0) {
						this.ruleForm = data.data;
						//解决前台上传图片后台不显示的问题
						let reg=new RegExp('../../../upload','g')//g代表全部
					} else {
						this.$message.error(data.msg);
					}
				});
			},

			// 提交
			async onSubmit() {
					if(this.ruleForm.yuyuebianhao) {
						this.ruleForm.yuyuebianhao = String(this.ruleForm.yuyuebianhao)
					}
					if(this.ruleForm.fengmian!=null) {
						this.ruleForm.fengmian = this.ruleForm.fengmian.replace(new RegExp(this.$base.url,"g"),"");
					}
					var objcross = this.$storage.getObj('crossObj');
					var table = this.$storage.getObj('crossTable');
					if(objcross!=null) {
						if(!this.ruleForm.shuliang&&this.ruleForm.shuliang!=0){
							this.$message.error("借用数量不能为空");
							return
						}
						if(objcross.shuliang<this.ruleForm.shuliang){
							this.$message.error("借用数量不足");
							return
						}
					}
					if(!this.ruleForm.id) {
						delete this.ruleForm.userid
					}
					await this.$refs["ruleForm"].validate(async valid => {
						if (valid) {
							if(this.type=='cross'){
								var statusColumnName = this.$storage.get('statusColumnName');
								var statusColumnValue = this.$storage.get('statusColumnValue');
								if(statusColumnName!='') {
									var obj = this.$storage.getObj('crossObj');
									if(statusColumnName && !statusColumnName.startsWith("[")) {
										for (var o in obj){
											if(o==statusColumnName){
												obj[o] = statusColumnValue;
											}
										}
										var table = this.$storage.get('crossTable');
										await this.$http({
											url: `${table}/update`,
											method: "post",
											data: obj
										}).then(({ data }) => {});
									}
								}
								// 跨表计算
								var objcross = this.$storage.getObj('crossObj');
								var table = this.$storage.get('crossTable');
							}
							let changeType = true
							let errMsg = ''
							await this.$http({
								url: `shebeiyuyue/${!this.ruleForm.id ? "save" : "update"}`,
								method: "post",
								data: this.ruleForm
							}).then(async ({ data }) => {
								if (data && data.code === 0) {
									this.$message({
										message: "操作成功",
										type: "success",
										duration: 1500,
										onClose: () => {
											this.parent.showFlag = true;
											this.parent.addOrUpdateFlag = false;
											this.parent.shebeiyuyueCrossAddOrUpdateFlag = false;
											this.parent.search();
										}
									});
								} else {
									this.$message.error(data.msg);
								}
							});
						}
					});
			},
			// 获取uuid
			getUUID () {
				return new Date().getTime();
			},
			// 返回
			back() {
				this.parent.showFlag = true;
				this.parent.addOrUpdateFlag = false;
				this.parent.shebeiyuyueCrossAddOrUpdateFlag = false;
			},
			fengmianUploadChange(fileUrls) {
				this.ruleForm.fengmian = fileUrls;
			},
		}
	};
